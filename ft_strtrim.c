#include "libft.h"

static int	ft_inset(char c, char const *set)
{
	int	i;

	i = 0;
	while (set[i] != '\0')
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = ft_strlen(s1);
	while (s1[i] != '\0' && ft_inset(s1[i], set))
		i++;
	while (j > i && ft_inset(s1[j - 1], set))
		j--;
	return (ft_substr(s1, i, j - i));
}
